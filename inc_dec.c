#include<stdio.h>
int main()
{
    int a=10,b=20;
    printf("---INCREMENT VALUE OPERATOR---\n");
    
    printf("value of a : %d\n",a++);
    printf("value of a : %d\n",a);

    printf("---DECREMENT VALUE OPERATOR---\n");

    printf("value of b : %d\n",b--);
    printf("value of b : %d\n",b);
    

}